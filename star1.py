def star(n):
    for i in range(0,n): #  i=4
        if i<(n)/2: # 2.5
            print("*"*(i+1)+" "*(n-2*i-1)+"*"*(i+1)) # ******
        else:
            print("*"*(n-i)+" "*(2*(i-(n//2)))+"*"*(n-i)) # **2sp**
star(5)
star(9)












